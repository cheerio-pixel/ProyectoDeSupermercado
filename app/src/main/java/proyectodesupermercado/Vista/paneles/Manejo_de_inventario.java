    private void doSearch() {
        if (busquedaTextfield.getText().isBlank()) {
            currentModel = controlInventario.refreshInitialModel();
        } else {
            currentModel = controlInventario.search(busquedaTextfield.getText());
        }
        mainTable.setModel(currentModel);
    }

    private void editSelectedProduct() {
        int index = TableUtils.getSelectedIndex(mainTable, "Debe de selecionar un producto");
        if (index == -1) {
            return;
        }
        InventarioProducto producto = currentModel.getRow(index);
//        if (cantidadProductoSpinner.getValue().equals(producto.getCantidad())) {
//            return;
//        }
        if (JOptionPane.NO_OPTION == JOptionPane.showConfirmDialog(this, "Â¿Quieres editar este producto?", "", JOptionPane.YES_NO_OPTION)) {
            return;
        }
        producto.setCantidad(((int) cantidadProductoSpinner.getValue()));
        currentModel.fireTableRowsUpdated(index, index);
        Optional<String> msg = controlInventario.editProduct(producto);
        if (msg.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Se cambio la cantidad con exito.");
        } else {
            JOptionPane.showMessageDialog(this, msg.get());
        }
    }

